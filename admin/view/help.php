<h3>Silver Lake United Methodist CMS Help Page</h3>
<br>
<p>This Content Management System delivers content to three areas of your website:
<ul>
	<li>1) Message From Mary</li>
	<li>2) Calendar</li>
	<li>3) Events</li>
</ul>
<br>
The <strong>most recent post</strong> in any category is displayed on its
corresponding page.<br>
<h4>Submitting a New Post:</h4>
<p>Begin by loading a template from <strong>Insert > Insert Template</strong>.<br>
or click this button on the toolbar: <br><img id="template_button" src="images/template_button.gif"><br>
Alter the template with any necessary updates. <br>
At any time, clicking the "save post" button will save your work.</p>
<p>
<strong>Note:</strong> The title field is for keeping track of your posts and is
 completely optional. The title will not appear in your finished content, but it
 will label the post in the sidebar of the editor. If you choose not to title 
 your post, the title will default to the date and time of the post.</p>
<p>
When you're satisfied with your content: <br>
click "submit post", <br>
select the proper destination, <br>
and click submit. <br>
</p>
<p>
Navigate to the appropriate page and check the results.</p>
<h4>The Sidebar:</h4>
<p>The sidebar at right lists recent posts, each with a heading that notes the
post's destination. Clicking on a post in the sidebar gives you the option to edit 
that post. Clicking the "X" on a post gives the option to delete the post. 
Be careful! Deletes are final.</p>
